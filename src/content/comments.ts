export function resolveDanmaku(keys: string[] | undefined, pickFn: (arr: string[]) => string): string[] {
  if (!keys || keys.length === 0) return []
  const out: string[] = []
  for (const k of keys) {
    if (k.startsWith('#')) {
      const pool = DANMAKU[k.slice(1)]
      if (pool && pool.length) {
        const used = new Set<string>()
        for (let i = 0; i < 3; i++) {
          const c = pickFn(pool)
          if (!used.has(c)) {
            used.add(c)
            out.push(c)
          }
        }
      }
    } else {
      out.push(k)
    }
  }
  return out
}
